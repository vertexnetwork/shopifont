function downloadTxt(kit: Kit): string {
  const fams = kit.singleFamily ? [kit.heading] : [kit.heading, kit.body];
  const blocks = fams
    .map((f) => {
      const files = expectedFiles(f)
        .map(
          (name, i) =>
            `   - ${f.family} ${f.weights[i]} (${
              f.weights[i] === 700 ? "Bold" : "Regular"
            })  →  rename the WOFF2 to:  ${name}`,
        )
        .join("\n");
      return `${f.family}
   Download page: ${f.googleFontsUrl}
   You need these weights as .woff2:
${files}`;
    })
    .join("\n\n");

  return `FONT FILES — ${kit.name}
${"=".repeat(48)}

Every font in this kit is licensed under the SIL Open Font License
(OFL). You may self-host and use it on a commercial Shopify storefront
for free. We don't ship the font binaries (that's how OFL self-hosting
is meant to work) — you fetch them once from Google Fonts and rename
them to the exact filenames the install code expects.

HOW TO GET WOFF2 FROM GOOGLE FONTS
1. Open the download page below for each family.
2. Click "Get font", then "Download all" — you'll get .ttf files.
3. Convert each .ttf you need to .woff2. Free, no signup:
      https://cloudconvert.com/ttf-to-woff2
   (or any TTF→WOFF2 converter — the format is what makes the file
   small and fast; this step is not optional).
4. Rename each .woff2 to the EXACT filename listed below. The install
   code references these names — a typo here is the only thing that
   can break the install.
5. Upload all of them to your theme's  assets/  folder.

${blocks}

That's every file. Total on your storefront: ${kit.fileBudget}.

Generated by Shopifont — shopifont.app
`;
}

