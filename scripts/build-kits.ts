/**
 * Builds the downloadable Shopify Typography Kit bundles into `kits/`.
 *
 * Every line of install code is produced by the SAME generator
 * functions the website ships (lib/generators/*), composed through the
 * battle-tested `fromSimple` legacy path. Nothing here hand-writes
 * @font-face or CSS-variable output — correctness is inherited from
 * the generator's own unit test suite. That matters because the
 * product ships and then nobody watches it for ten months.
 *
 * Regenerable + deterministic: the script wipes `kits/` and rebuilds
 * from content/kits.ts + content/themes.ts. Run:
 *   npx tsx scripts/build-kits.ts
 *
 * Output per kit: a README, a printable specimen, font-download
 * instructions, a license/usage sheet, an uninstall sheet, and a
 * per-theme install folder (font-face.css, css-variables.css,
 * settings_schema.snippet.json, preload.liquid, INSTALL.txt) for all
 * 13 free Shopify OS 2.0 themes.
 */

import { mkdirSync, rmSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import {
  buildCssVariableOverrides,
  buildFontFaceCss,
  fromSimple,
  slugify,
  type SimpleGeneratorInput,
} from "../lib/generators";
import { KITS, type Kit, type KitFont } from "../content/kits";
import { THEMES } from "../content/themes";

const __dirname = dirname(fileURLToPath(import.meta.url));
const projectRoot = resolve(__dirname, "..");
const OUT = resolve(projectRoot, "kits");

/** One generator input per family. Regular(400) + Bold(700). */
function inputFor(
  font: KitFont,
  applyTo: ReadonlyArray<"heading" | "body">,
): SimpleGeneratorInput {
  const [primary, ...rest] = font.weights;
  return {
    fontName: font.family,
    formats: ["woff2"],
    weight: primary,
    style: "normal",
    fileBaseName: slugify(font.family),
    applyTo,
    additionalWeights: rest.length > 0 ? rest : undefined,
  };
}

/** The exact WOFF2 filenames the generated @font-face url()s expect. */
function expectedFiles(font: KitFont): string[] {
  const base = slugify(font.family);
  return font.weights.map((w) => `${base}-${w}.woff2`);
}

function fontFaceFor(kit: Kit): string {
  if (kit.singleFamily) {
    return buildFontFaceCss(
      fromSimple(inputFor(kit.heading, ["heading", "body"])),
    );
  }
  return [
    `/* Heading family: ${kit.heading.family} */`,
    buildFontFaceCss(fromSimple(inputFor(kit.heading, ["heading"]))),
    "",
    `/* Body family: ${kit.body.family} */`,
    buildFontFaceCss(fromSimple(inputFor(kit.body, ["body"]))),
  ].join("\n");
}

function cssVarsFor(kit: Kit): string {
  if (kit.singleFamily) {
    return buildCssVariableOverrides(
      fromSimple(inputFor(kit.heading, ["heading", "body"])),
    );
  }
  return [
    `/* Heading family → heading CSS variables */`,
    buildCssVariableOverrides(fromSimple(inputFor(kit.heading, ["heading"]))),
    "",
    `/* Body family → body CSS variables */`,
    buildCssVariableOverrides(fromSimple(inputFor(kit.body, ["body"]))),
  ].join("\n");
}

function allExpectedFiles(kit: Kit): string[] {
  if (kit.singleFamily) return expectedFiles(kit.heading);
  return [...expectedFiles(kit.heading), ...expectedFiles(kit.body)];
}

function w(path: string, body: string): void {
  const full = resolve(OUT, path);
  mkdirSync(dirname(full), { recursive: true });
  writeFileSync(full, body.replace(/\r\n/g, "\n"), "utf8");
}

