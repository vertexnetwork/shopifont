function main(): void {
  rmSync(OUT, { recursive: true, force: true });
  mkdirSync(OUT, { recursive: true });

  let fileCount = 0;
  const wc = (p: string, b: string) => {
    w(p, b);
    fileCount++;
  };

  wc("README.txt", topReadme());

  for (const kit of KITS) {
    const root = kit.slug;
    wc(`${root}/README.txt`, kitReadme(kit));
    wc(`${root}/SPECIMEN.html`, specimenHtml(kit));
    wc(`${root}/fonts/DOWNLOAD.txt`, downloadTxt(kit));
    wc(`${root}/LICENSE-AND-USAGE.txt`, licenseTxt(kit));
    wc(`${root}/UNINSTALL.txt`, uninstallTxt(kit));

    const fontFace = fontFaceFor(kit);
    const cssVars = cssVarsFor(kit);

    for (const theme of THEMES) {
      const dir = `${root}/install/${theme.slug}`;
      wc(`${dir}/font-face.css`, fontFace + "\n");
      wc(`${dir}/css-variables.css`, cssVars + "\n");
      wc(
        `${dir}/INSTALL.txt`,
        installTxt(
          kit,
          theme.name,
          theme.injectionPoint,
          theme.notableSelector,
        ),
      );
    }
  }

  // eslint-disable-next-line no-console
  console.log(
    `[build-kits] wrote ${fileCount} files for ${KITS.length} kits × ${THEMES.length} themes → ${OUT}`,
  );
}

main();
